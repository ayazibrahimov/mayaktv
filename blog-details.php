                                         uğrunda səylər təbəqələşmənin daha da sərtləşməsinə səbəb ola bilər. Məsələ orasındadır ki, aşağıların yuxarılarla bərabərləşmə cəhdi orta sinfi təhlükə altına atır. Vaxtı ilə Avropanı marksizmdən
                                          qoruyan əsas amil məhz orta sinfin formalaşması oldu. Dövlətin müqəddəratını və dayanıqlığını təyin edən çoxluq orta yaşayış səviyyəsinə
                                          çatdırıldı, çox zəngin və çox fəqirlər isə azlıqda qaldı. Amma müasir texnologiyaların inkişafı, 
                                         iqtisadi dəyişikliklər və yeni məşğuliyyət növlərinin formalaşması orta sinifi aradan aparmaqdadır.
                                          Yəni öyündüyümüz inkişaf heç də həyatımıza birmənalı təsir göstərmir. Mütəfəkkirləri də maraqlandıran budur: texnoloji inkişaf hansı cəmiyyət modelinin yaranmasını vəd edir?

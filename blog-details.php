                                         iqtisadi dəyişikliklərə asan adaptasiya olan insanlar aiddir. Yeni elitalar kompyuter texnologiyaları, dizayn və digər əlaqəli sahələrdə
                                          çalışan şəxslərdən ibarət olacaq. Eyni zamanda aşağı təbəqənin də konturları cızılır. 
                                         Bu ilk növbədə yaxşı təhsil ala bilməyən və xüsusi bacarıq tələb etməyən işlərdə çalışan şəxslərdir. Məsələn,
                                          taksi sürücüləri, kuryerlər, xadimələr və s. Pandemiya zamanı Rekvitsin haqlı olduğunu görmək olardı. 
                                         Bəziləri evində oturub yüksək maaşla çalışırdılar, bəziləri isə əlacsızlıqdan onlara ucuz qiymətə xidmət göstərirdilər: yemək gətirir, paltar yuyur, ərzaq çatdırır, ev təmizləyirdilər. Bu baxımdan pandemiya 
                                         sosial dəyişikliklərin katalizatoru rolunu oynadı. Dünən orta təbəqəyə aid olan insanlar, bu gün dolanışığını təmin etmək üçün çabalayırlar. Beləcə, sosial dəyişikliklərə həssas yanaşan böyük bir kütlə formalaşdı. Ona görə də, 
                                         sosial bərabərsizlik və qaranlıq gələcək mövzusunda filmlər daha çox izləyici kütləsi toplaya bilir.
